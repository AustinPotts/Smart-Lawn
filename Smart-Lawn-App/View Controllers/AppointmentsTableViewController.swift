    // MARK: - Navigation

    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        if segue.identifier == "AppointDetailSegue" {
            guard let detailVC = segue.destination as? ScheduleViewController,
                let indexPath = tableView.indexPathForSelectedRow else {return}
            
            detailVC.appointment = appointment[indexPath.row]
            detailVC.appointmentController = appointmentController
        } else if segue.identifier == "AddAppointSegue" {
            guard let detailVC = segue.destination as? ScheduleViewController else{return}
            
            detailVC.appointmentController = appointmentController
        }
    }
    

